<h2 id="qu-est-ce-que-le-metier-de-chef-de-projet">Qu&#39;est-ce que le metier de chef de projet</h2>
<p>Le chef de projet a pour mission principale de prendre en main le cahier des charges d’un projet et de le mettre en œuvre en analysant les besoins, réalisant des reportings… 
Le travail en équipe est primordial pour ce professionnel. En effet, le directeur de projet est en relation avec les différents employés d’une entreprise (graphistes, assistants-chefs de projet, chefs de produit, développeurs web, intégrateurs…) et leur confie des missions pour mener à bien le projet.</p>
<p>Ses missions sont :</p>
<ul>
<li>il élabore un cahier des charges précis et prépare un calendrier précis des différentes étapes du projet</li>
<li>il travaille en collaboration avec les clients pour connaître leurs attentes</li>
<li>il organise des réunions avec les différents salariés pour connaître leur expertise sur des sujets qui concernent son projet</li>
<li>il prend en compte les délais afin de respecter la feuille de route de son projet</li>
</ul>
<h2 id="mais-alors-qu-est-ce-qu-un-chef-de-projet-numerique-">Mais alors qu&#39;est-ce qu&#39;un chef de projet numerique ?</h2>
<p>Le Chef de Projet Numérique est le coordinateur de l’ensemble des étapes relatives à la réalisation d’un projet internet ou mobile : généralement sur des projets de sites web ou d’applications mobiles. Mais il peut également intervenir sur bien d’autres projets comme des bases de données, du brand content, de la création de contenu, du référencement, des programmes d’affiliation, ou encore toute spécificité du secteur du webmarketing…
Ce chef d’orchestre polyvalent fait le lien entre les différentes équipes de l’entreprise dans laquelle il est en poste et les partenaires externes au projet.</p>
