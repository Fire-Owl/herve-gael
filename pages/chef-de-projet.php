<h2 id="est-je-les-qualit-s-et-comp-tences-n-cessaires-">Est-je les qualités et compétences nécessaires ?</h2>
<p>  Il doit <strong>prendre en compte les compétences de chacun</strong> et leur donner des missions cohérentes avec cela. À l’écoute, il possède de grandes capacités d’organisation. Il doit également être <strong>rigoureux</strong>. C’est même le garant de la rigueur en termes de temps, de budget, de ressources… du projet. 
  Très intéressé par le domaine des technologies, il a des <strong>compétences techniques en informatique</strong> et sait ainsi de quoi il parle avec ses collaborateurs. Ce sont les formations informatiques, ingénieurs, ou encore jeux vidéos qui donnent ces compétences au chef de projet numérique.</p>
<h2 id="o-puis-excercer-cette-activit-paris-lyon-ou-a-la-campagne-">Où puis excercer cette activité ? Paris, Lyon ou a la campagne ?</h2>
